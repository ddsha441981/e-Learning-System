package org.spring.Beans;

/***
 * @author : Deendayal
 * 
 * @param :
 *            Bean to set object of TrainerBeans Beans class
 * 
 * @return : Getter and Setter data set
 * 
 * @ since :June 30-2017
 */
public class TrainerBeans {

	private int trainerId;
	private String emailId;
	private String password;
	private String role;
	private String fname;
	private String lanme;
	private String country;
	private String state;
	private String city;
	private String dob;
	private String gender;
	private String qualification;
	private String address;

	public int getTrainerId() {
		return trainerId;
	}

	public void setTrainerId(int trainerId) {
		this.trainerId = trainerId;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getLanme() {
		return lanme;
	}

	public void setLanme(String lanme) {
		this.lanme = lanme;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDob() {
		return dob;
	}

	public void setDob(String dob) {
		this.dob = dob;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getQualification() {
		return qualification;
	}

	public void setQualification(String qualification) {
		this.qualification = qualification;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

}
